    {
        $json_file = Storage::disk('public')->files('machines_json');

        foreach ($json_file as $file) {

            $json_file_content[basename($file)] = Storage::disk('public')->get($file);

        }

        return view('settings.form', [

            'json_file_content' => $json_file_content

